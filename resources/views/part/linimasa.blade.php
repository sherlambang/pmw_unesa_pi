            PMW merupakan sebuah program yang diselenggarakan oleh Pemerintah melalui Direktorat Jenderal Pembelajaran dan Kemahasiswaan Kemenristekdikti (Dirjen Belmawa) sejak 2009 dengan tujuan untuk menciptakan dan mengembangkan entrepreneur terdidik yang berasal dari berbagai bidang keilmuan.
            
            <br><br>

            Program Mahasiswa Wirausaha (PMW) merupakan program pengembangan keterampilan mahasiswa dalam berwirausaha dan berorientasi pada laba (profit). PMW dilaksanakan oleh PTN, PTS dan Kopertis sesuai dengan alokasi dana yang berbeda-beda. PMW bertujuan memberikan bekal pengetahuan, keterampilan, sikap dan jiwa wirausaha (enterpreneurship) serta menciptakan calon pengusaha tangguh dalam persaingan global.

            <br><br>

            Keberhasilan pelaksanaan PMW dapat dilihat melalui 3 indikator yaitu (1) peran serta mahasiswa mengikuti PMW; (2) keberhasilan mahasiswa menjalankan usahanya sebagai wirausaha; dan (3) terbentuknya modal usaha sebagai model pendidikan kewirausahaan dan lembaga pengembangan kewirausahaan yang dapat mengkoordinasikan kegiatan kewirausahaan di perguruan tinggi. Diharapkan pedoman dan panduan ini dapat mendukung lancarnya pelaksanaan PMW di Universitas Negeri Surabaya.
            
